    /**
     * 第一个不为空的值
     * @param v1 v1
     * @param v2 v2
     * @return 值
     */
    public static <T>T firstNonNull(T v1, T v2) {
        return null == v1 ? v2 : v1;
    }


    /**
     * 是否任意为空
     * @param args 参数
     * @return 是否任意为空
     */
    public static boolean isAnyEmpty(Object... args) {
        for (Object arg : args) {
            if(null == arg) {
                return true;
            }

            if (arg instanceof String && StringUtils.isNullOrEmpty((String) arg)) {
                return true;
            }
        }

        return false;
    }
